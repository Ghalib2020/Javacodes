package javapackage;

public class Breakncontinue {

	public static void main(String[] args) {
		
		for(int i=0;i<10;i++) {
			if (i==3) { 
				// i=1+1=1 i=1+1=2;
		break;

	 		}
			System.out.println(i);
		}
		for(int x=0;x<10;x++) {
			if (x==3) {
				continue;}
				System.out.println(x);
				
			}
		
			
		}
		}
	


