package javapackage;

public class Arithmatic {

	public static void main(String[] args) {
		//addition
		int a=2;
		double b=25.1;

		double sum=a+b;
		System.out.println("The value is " +sum);
		
		//Subtraction
		double sub=b-a;
		System.out.println(sub);
		//division
		
		double div=b/a;
		System.out.println(div);
		
		//Modulus 
		
		double mud=b%a;
		System.out.println(mud);
		
		//mult
		
		double ml=a*b;
		System.out.println(ml);
		
		}

	}


